# Definição das variáveis da pessoa
nome = "Andre Lucas"
altura = 1.80  # em metros
peso = 88      # em kg

# Cálculo do IMC (Índice de Massa Corporal)
imc = peso / (altura ** 2)

# Exibição formatada do resultado
print(f"Nome: {nome}")
print(f"Altura: {altura:.2f} m")
print(f"Peso: {peso} kg")
print(f"IMC: {imc:.2f}")